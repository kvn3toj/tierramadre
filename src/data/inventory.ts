// Get only available items
export const getAvailableInventory = (): InventoryItem[] => {
  return inventoryData.filter(item => item.estado === 'DISPONIBLE');
};

// Get inventory stats
export const getInventoryStats = () => {
  const available = inventoryData.filter(i => i.estado === 'DISPONIBLE');
  const sold = inventoryData.filter(i => i.estado === 'VENDIDA');
  const totalValue = available.reduce((sum, i) => sum + i.precioCOP, 0);
  const soldValue = sold.reduce((sum, i) => sum + i.precioCOP, 0);
  const looseStones = available.filter(i => !i.isJewelry);
  const jewelry = available.filter(i => i.isJewelry);
  const goldItems = jewelry.filter(i => i.metalType === 'Oro 18k');
  const silverItems = jewelry.filter(i => i.metalType === 'Plata');

  return {
    totalItems: available.length,
    totalValue,
    looseStones: looseStones.length,
    jewelry: jewelry.length,
    goldItems: goldItems.length,
    silverItems: silverItems.length,
    soldItems: sold.length,
    soldValue,
  };
};

// Get unique colors
export const getUniqueColors = (): string[] => {
  return [...new Set(inventoryData.map(i => i.color))];
};

// Get unique qualities
export const getUniqueQualities = (): string[] => {
  return [...new Set(inventoryData.map(i => i.calidad))];
};

// Get unique cuts
export const getUniqueCuts = (): string[] => {
  return [...new Set(inventoryData.map(i => i.talla).filter(t => t && t !== '-'))];
};
