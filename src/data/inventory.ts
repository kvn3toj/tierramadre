// Parse price string to number - exported for external use
export const parsePrice = (price: string): number => {
  if (!price || price === '') return 0;
  const cleaned = price.replace(/[$,\s]/g, '').replace(/\./g, '');
  return parseInt(cleaned, 10) || 0;
};

// Parse peso (weight) - can be carats or metal type
export const parsePeso = (peso: string): { value: string | number; isJewelry: boolean; metalType?: 'Plata' | 'Oro 18k' } => {
  if (!peso) return { value: 0, isJewelry: false };
  const pesoStr = peso.toString().trim();

  if (pesoStr.toLowerCase().includes('plata')) {
    return { value: 'Plata', isJewelry: true, metalType: 'Plata' };
  }
  if (pesoStr.toLowerCase().includes('oro')) {
    return { value: 'Oro 18k', isJewelry: true, metalType: 'Oro 18k' };
  }

  // Try to parse as number (carats)
  const numValue = parseFloat(pesoStr.replace(',', '.'));
  return { value: isNaN(numValue) ? pesoStr : numValue, isJewelry: false };
};

